A dificuldade dos desafios é progressiva. E eles se tornam cada vez mais complexos. Mas isso não é um problema. Você sempre pode consultar suas soluções, tirar dúvidas e aprender com outros usuários na comunidade da plataforma.
O mais legal é que, de acordo com nível que você alcançar, poderá criar seus próprios desafios e disponibilizá-los para outros usuários do site tentarem resolver. É uma troca de experiências bastante enriquecedora.
